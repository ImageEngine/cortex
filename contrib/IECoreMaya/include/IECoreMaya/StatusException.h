#ifndef IE_COREMAYA_STATUSEXCEPTION_H
#define IE_COREMAYA_STATUSEXCEPTION_H

#include "IECore/Exception.h"

#include "maya/MStatus.h"

namespace IECoreMaya
{

/// This class provides a means of representing
/// MStatus objects as exceptions, which might
/// make for easier programming at times. In particular
/// it can be thrown in python bindings and it'll be
/// picked up and turned into a python exception.
class StatusException : public IECore::Exception
{

	public :

		/// Constructs an exception to represent
		/// the given maya status code. It might be daft
		/// to make one representing MStatus::kSuccess,
		/// but no attempt is made to prevent that.
		StatusException( const MStatus &status );

		virtual const char *type() const throw();

		/// If status represents an error, throws StatusException( status ),
		/// otherwise does nothing and returns.
		static void throwIfError( const MStatus &status );
		
};

} // namespace IECoreMaya

#endif // IE_COREMAYA_STATUSEXCEPTION_H
