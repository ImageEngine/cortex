#ifndef IE_COREMAYA_MATRIXTRAITS_H
#define IE_COREMAYA_MATRIXTRAITS_H

#include "IECore/MatrixTraits.h"

#include "maya/MMatrix.h"
#include "maya/MFloatMatrix.h"

namespace IECore
{

template<>
struct MatrixTraits<MMatrix>
{
	typedef double BaseType;
	static unsigned int dimensions() { return 4; };
	static BaseType get( const MMatrix &v, unsigned int i, unsigned int j ) { return v[i][j]; };
	static void set( MMatrix &v, unsigned int i, unsigned int j, BaseType x ) { v[i][j] = x; };
};

template<>
struct MatrixTraits<MFloatMatrix>
{
	typedef float BaseType;
	static unsigned int dimensions() { return 4; };
	static BaseType get( const MFloatMatrix &v, unsigned int i, unsigned int j ) { return v[i][j]; };
	static void set( MFloatMatrix &v, unsigned int i, unsigned int j, BaseType x ) { v[i][j] = x; };
};

} // namespace IECore

#endif // IE_COREMAYA_MATRIXTRAITS_H
