#ifndef IE_COREMAYA_MSTRINGLESS_H
#define IE_COREMAYA_MSTRINGLESS_H

#include "maya/MString.h"

namespace std
{

/// A specialisation of less<> for MString, allowing it to be easily
/// used as the key in std::map (and a bunch of other places too no doubt).
/// Doing things in the std namespace does seem a little iffy but we're only
/// specialising for a type which has nothing to do with the std, so I
/// don't see how it can be bad.
template<>
struct less<MString> : binary_function<MString, MString, bool>
{
	bool operator()( const MString &a, const MString &b ) const
	{ 
		return strcmp( a.asChar(), b.asChar() ) < 0;
	}
};

} // namespace std

#endif // IE_COREMAYA_MSTRINGLESS_H
