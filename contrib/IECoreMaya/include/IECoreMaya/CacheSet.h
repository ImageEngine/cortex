#ifndef IE_COREMAYA_CACHESET_H
#define IE_COREMAYA_CACHESET_H

#include "maya/MPxObjectSet.h"
#include "maya/MStringArray.h"

namespace IECoreMaya
{
/// A Maya set base node which encapsulates basic funtionality required of
/// cache sets.
class CacheSet : public MPxObjectSet
{
	public:
		CacheSet();
		virtual ~CacheSet();
		
		static void *creator();
		
		static MStatus initialize();
		
		virtual bool isAbstractClass() const;
		virtual MStatus compute(const MPlug &plug, MDataBlock &block);		
		
		static MTypeId id;
		
		static MObject aActive;
		static MObject aFrameRate;
		static MObject aOversamples;
		static MObject aActualOversamples;
		static MObject aOutFrameMel;
		
	protected:
	
		/// A simple helper function to translate an MStringArray into
		/// the MEL representation of the same data.
		MString melFromStringArray(const MStringArray &a) const;
		
};
}

#endif // IE_COREMAYA_CACHESET_H
